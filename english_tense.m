Past perfect

.PP
Sarah went to a party last week. Paul went to the party too but they didn't see each other. Paul went home at 10.30 and Sarah arrived at 11 o'clock. So:

When Sarah arrived at the party, Paul wasn't there. He had gone home.

Sometimes we talk about something that happened in the past:

Sarah arrived at the party.

This is the starting point of the story. Then, if we want to talk about things that happened before this time, we use the past perfect.

When Sarah arrived at the party, Paul had already gone home.

When we got home last night, we found that somebody had broken into the flat.

Karen didn't want to come to the cinema with us because she had already seen the film.

At first I thought I'd done the right thing, but I soon realised that I'd made a serious mistake.

(I'd done happened before I thought. I'd made a serious mistake happened before I realised)

The man sitting next to me on the plane was very nervous. He hadn't flown before. (He had never flown before)

comparing with present perfect:

Who is that woman? I've never seen her before.

We aren't hungry. We've just had lunch.

The house is dirty. They haven't cleaned it for weeks.

I didn't know who she was. I'd never seen her before.

We weren't hungry. We'd just had lunch.

The house was dirty. They hadn't cleaned it for weeks.


Example from The mayor of casterbridge:

One evening of late summer, before the nineteenth century had reached one-third of its span.

When the story was told, the nineteenth century had already reached one-third of its span. This summer was the time before that.

