Linear predictors

.PP
Many learning algorithms that are being widely used in practice rely on linear predictors.

Several hypothesis classes belonging to this family - halfspaces, linear regression predictors, and logistic regression predictors.

Relevant learning algorithms: linear programming, and the Perceptron algorithm for the class of halfspaces, and the Least Squares algorithm for linear regression.

