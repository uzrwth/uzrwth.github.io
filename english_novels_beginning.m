The schoolmaster was leaving the village, and everybody seemed sorry. The miller at Cresscombe lent him the small white tilted cart and horse to carry his goods to the city of his destination, about twenty miles off, such a vehicle proving of quite sufficient size for the departing teacher's effects.


A little boy of eleven, who had been thoughtfully assisting in the packing, joined the group of men, and as they rubbed their chins he spoke up, blushing at the sound of his own voice: "Aunt have got a great fuel-house, and it could be put there, perhaps, till you've found a place to settle in, sir."

"A proper good notion," said the blacksmith.

It was decided that a deputation should wait on the boy's aunt -- an old maiden resident -- and ask her if she would house the piano till Mr. Phillotson should send for it. The smith and the bailiff started to see about the practicability of the suggested sheltered, and the boy and the schoolmaster were left standing alone.

"Sorry I am going, Jude?" asked the latter kindly.

Tears rose into the boy's eyes, for he was not among the regular day scholars, who came unromantically close to the schoolmaster's life, but one who had attended the night school only during the present teacher's term of office. The regular scholars, if the truth must be told, stood at the present moment afar off, like certain historic disciples, indisposed to any enthusiastic volunteering of aid.

The boy awkwardly opened the book he held in his hand, which Mr. Phillotson had bestowed on him as a parting gift, and admitted that he was sorry.

"So am I," said Mr. Phillotson.

"Why do you go, sir?" asked the boy.

"Ah--that would be a long story. You wouldn't understand my reasons, Jude. You will, perhaps, when you are older."

"I think I should now, sir."

"Well--don't speak of this everywhere. You know what a university is, and a university degree? It is the necessary hallmark of a man who wants to do anything in teaching. My scheme, or dream, is to be a university graduate, and then to be ordained. By going to live at Christminster, or near it, I shall be at headquarters, so to speak, and if my scheme is practicable at all, I consider that being the spot will afford me a better chance of carrying it out than I should have elsewhere."

