One evening of late summer, before the nineteenth century had reached one-third of its span, a young man and woman, the latter carrying a child, were approaching the large village of Weydon-Priors, in Upper Wessex, on foot.

They were plainly but not ill clad, though the thick hoar of dust which had accumulated on their shoes lent a disadvantageous shabbiness to their appearance just now.

The man was of fine figure, swarthy, and stern in aspect; and he showed in profile a facial angle so slightly inclined as to be almost perpendicular.

At his back he carried by a looped strap a rush basket, from which protruded at one end the crutch of a hay-knife, a wimble for hay-bonds being also visible in the aperture. His measured, springless walk was the walk of the skilled country-man as distinct from the desultory shamble of the general labourer; while in the turn and plant of each foot there was, further, a dogged and cynical indifference personal to himself, showing its presence even in the regularly interchanging fustian folds, now in the left leg, now in the right, as he paced along.

(Why at his back but not on his back?)


What was really peculiar, however, in this couple's progress, and would have attracted the attention of any casual observer otherwise disposed to overlook them, was the perfect silence they preserved.

but on closer view it could be discerned that the man was reading, or pretending to read, a ballad sheet which he kept before his eyes with some difficulty by the hand that was passed through the basket strap.


Whether this apparent cause were the real cause, or whether it were an assumed one to escape an intercourse that would have been irksome to him, nobody but hiimself could have said precisely; but his taciturnity was unbroken, and the woman enjoyed no society whatever from his presence.

Virtually she walked the highway alone, save for the child she bore.

(The usage of virtually and save for: virtually: literally, save for: except)

Sometimes the man's bent elbow almost touched her shoulder, for she kept as close to his side as was possible without actual contact, but she seemed to have no idea of taking his arm, nor he of offering it; and far from exhibiting surprise at his ignoring silence she appeared to receive it as a natural thing. It any word at all were uttered by the little group, it was an occasional whisper of the woman to the child -- a tiny girl in short clothes and blue boots of knitted yarn -- and the murmured babble of the child in reply.

That the man and woman were husband and wife, and the parents of the girl in arms there could be little doubt. No other than such relationship would have accounted for the atmosphere of stale familiarity which the trio carried along with them like a nimbus as they moved down the road.



