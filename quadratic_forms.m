.PP
Quadratic forms occur frequently in applications of linear algebra to engineering (optimization) and signal processing (as output noise power). They also arise in physics (as potential and kinetic energy), differential geometry (as normal curvature of surfaces), economics (as utility functions), and statistics (in confidence ellipsoids)

.PP
A quadratic form is a function Q whose value at a vector x can be computed by an expression of the form Q(x) = xAx.