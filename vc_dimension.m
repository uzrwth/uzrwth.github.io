VC Dimension

.PP
A hypothesis set might not be able to fully realize the labeling of a set of size m.

Then we decrease m slowly. We try m-1, m-2, ... until we found d = m-k, such that the hypothesis set fully realizes d points in a set. Then the VC dimension of the hypothesis set H is d.

The relationship between a growth function and VC dimension: for the hypothesis set, we search for a growth function such that its value is 2^m and m is the maximum we could achieve.